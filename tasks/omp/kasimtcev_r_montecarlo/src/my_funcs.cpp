#include <cmath>

double kasimtcev_flinear(double x, double y) { return x + y; }
double kasimtcev_fsinxsiny(double x, double y) { return sin(x) + sin(y); }
double kasimtcev_fcosxcosy(double x, double y) { return cos(x) + cos(y); }
double kasimtcev_fxy(double x, double y) { return x * y; }
double kasimtcev_fxyy(double x, double y) { return x * y * y; }